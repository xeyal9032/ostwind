const xeyalByLoc = { az: xeyalAz, tr: xeyalTr, en: xeyalEn, ru: xeyalEn, uk: xeyalEn, ge: xeyalEn };

function pick(section, loc) {
  return patches[section][loc] ?? patches[section].en ?? patches[section].tr;
}

for (const loc of locales) {
  const file = path.join('messages/admin', `${loc}.json`);
  const data = JSON.parse(fs.readFileSync(file, 'utf8'));
  Object.assign(data.common, pick('common', loc));
  Object.assign(data.universities, pick('universities', loc));
  Object.assign(data.team, pick('team', loc));
  Object.assign(data.blog, pick('blog', loc));
  Object.assign(data.pricing, pick('pricing', loc));
  Object.assign(data.about, pick('about', loc));
  Object.assign(data.contact, pick('contact', loc));
  const extra = xeyalByLoc[loc];
  for (const [k, v] of Object.entries(extra)) {
    data.xeyal[k] = { ...(data.xeyal[k] || {}), ...v };
  }
  fs.writeFileSync(file, `${JSON.stringify(data, null, 2)}\n`);
  console.log('patched', loc);
}
